#include<iostream>
using namespace std;
int main(){

    int x = 4;
    cout<<"Left shift "<<(x << 1)<<endl; // 1 bit shift from right to left
    cout<<"Left shift "<<(x << 2)<<endl; // 2 bit shift from right to left
    cout<<"Left shift "<<(x << 3)<<endl; // 3 bit shift from right to left
    
    

    return 0;
}