#include<iostream>
using namespace std;
int main(){

    int x = 5;
    cout<<"Left shift "<<(x >> 1)<<endl; // 1 bit shift from left to right 
    cout<<"Left shift "<<(x >> 2)<<endl; // 2 bit shift from left to right 
    cout<<"Left shift "<<(x >> 3)<<endl; // 3 bit shift from left to right 
    
    

    return 0;
}